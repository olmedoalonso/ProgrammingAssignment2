## There may be some benefit to caching the inverse of a matrix rather than
## compute it repeatedly because it usually is a costly computation. 

## Write a short comment describing this function
## This function creates a special "matrix" object that can cache its inverse


makeCacheMatrix <- function(x = matrix()) {
   inv <- NULL
   set <- function(y) {
          x <<- y
          inv <<- NULL
          }
   get <- function() {x}
   setinv <- function(inverse) inv <<- inverse
   getinv <- function() {inv}
   list(set = set, get = get, 
        setinv = setinv,
        getinv = getinv)
}

## Write a short comment describing this function
## This function computes the inverse of the special "matrix" returned by
## makeCacheMatrix. If the inverse has already been calculated and the
## matrix has not changed, it should retrieve the inverse from the cache.

cacheSolve <- function(x, ...){
   inv <- x$getinv()
   if(!is.null(inv)){
      message("getting cached data")
      return(inv)
   }
   mat <- x$get()
   inv <- solve(mat,...)
   x$setinv(inv)
   inv
}

